package com.feipinjia.model;

public class Config {
	private int id;
	private String title;
	private String desc;
	private int type;
	private int order;
	private int exta;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getOrder() {
		return order;
	}
	public void setOrder(int order) {
		this.order = order;
	}
	public int getExta() {
		return exta;
	}
	public void setExta(int exta) {
		this.exta = exta;
	}
}
